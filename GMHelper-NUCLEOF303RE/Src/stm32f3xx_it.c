/**
  * @brief This function handles TIM7 global interrupt.
  */
void TIM7_IRQHandler(void)
{
  /* USER CODE BEGIN TIM7_IRQn 0 */

  /* USER CODE END TIM7_IRQn 0 */
  HAL_TIM_IRQHandler(&htim7);
  /* USER CODE BEGIN TIM7_IRQn 1 */
  ADCKeyboard_tick_it();
  Bomb_tick_it();
  PointCapture_tick_it();
  if(dv_200Hz-- == 0)
  {
	  dv_200Hz = DV_200HZ;
	  Buzzer_note_duration_it();
	  if(dv_5Hz-- == 0)
	  {
		  dv_5Hz = DV_5HZ;
		  Bomb_blink_tick_it();
		  PointCapture_tick_blink_it();
		  if(dv_1Hz-- == 0)
		  {
			  dv_1Hz = DV_1HZ;
			  Janitor_tick_it();
			  PointCapture_clock_it();
			  Bomb_clock_tick_it();
		  }
	  }
  }
  /* USER CODE END TIM7_IRQn 1 */
}

